## 리스트의 기본
## 리스트의 필요성
a,b,c,d=0,0,0,0 # 입력박을 변수 선언
hap=0           # 결과값 변수 선언

a=int(input("1번째 숫자 : ")) # 1번째 숫자 입력
b=int(input("2번째 숫자 : ")) # 2번째 숫자 입력
c=int(input("3번째 숫자 : ")) # 3번째 숫자 입력
d=int(input("4번째 숫자 : ")) # 4번째 숫자 입력

hap=a+b+c+d # 입력받은 정수 합계

print("합계 ==> %d" %hap) # 결과 출력
