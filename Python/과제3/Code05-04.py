##기본 if문
##if~else문

a = 200 # a에 200 대입

if a < 100 :                            # a < 100 참일때
    print("100보다 작군요.")            # 내용출력
    print("참이면 이 문장도 보이겠죠?") # 내용출력
else :                                   # a < 100 거짓일때
    print("100보다 크군요.")             # 내용출력
    print("거짓이면 이 문장도 보이겠죠?")# 내용출력


print("프로그램 끝") # if문과 상관없이 내용 출력

